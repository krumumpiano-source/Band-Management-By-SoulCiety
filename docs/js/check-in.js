  var slots = ciGetSlotsForDate(date);
  var existingSlots = (ciExistingCheckIn && ciExistingCheckIn.slots) ? ciExistingCheckIn.slots : [];

  if (noSlotsMsg) noSlotsMsg.style.display = 'none';

